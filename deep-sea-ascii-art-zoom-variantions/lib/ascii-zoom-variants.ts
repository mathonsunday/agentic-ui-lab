// Helper type for zoom levels
export type ZoomLevel = "far" | "medium" | "close"

// Helper to get art at specific zoom
export function getArtAtZoom(artName: keyof typeof allZoomVariants, zoom: ZoomLevel): string {
  return allZoomVariants[artName][zoom]
}
